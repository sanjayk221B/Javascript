// What are arrays?
// Arrays are ordered collections that can store multiple values of any data type

let simpleArray = [1, 2, 3, 4, 5];
let mixedArray = [1, "hello", true, { name: "John" }, [1, 2, 3]];

// Why use arrays?
// 1. Organize related data
let colors = ["red", "green", "blue"];

// 2. Store collections
let todoList = ["Learn JavaScript", "Build Projects", "Practice Coding"];  

// 3. Maintain order
let steps = ["First", "Second", "Third"];

// Array characteristics
// - Ordered collection
// - Zero-based indexing
// - Dynamic size
// - Can hold mixed data types
// - Provide built-in methods for manipulation