// Arithmetic Operators in JavaScript

// Addition (+)
let sum = 5 + 3;
console.log('5 + 3 =', sum); // Output: 8

// Subtraction (-)
let difference = 10 - 4;
console.log('10 - 4 =', difference); // Output: 6

// Multiplication (*)
let product = 6 * 7;
console.log('6 * 7 =', product); // Output: 42

// Division (/)
let quotient = 20 / 4;
console.log('20 / 4 =', quotient); // Output: 5

// Modulus (%)
let remainder = 17 % 5;
console.log('17 % 5 =', remainder); // Output: 2

// Exponentiation (**)
let power = 2 ** 3;
console.log('2 ** 3 =', power); // Output: 8

// Increment (++)
let num = 5;
num++;
console.log('After increment:', num); // Output: 6

// Decrement (--)
num--;
console.log('After decrement:', num); // Output: 5

// Order of operations
let result = 10 + 5 * 2;
console.log('10 + 5 * 2 =', result); // Output: 20

